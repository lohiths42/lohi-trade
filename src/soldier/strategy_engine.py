"""
Strategy Engine for LOHI-TRADE.

Defines the abstract Strategy interface and Signal data structure for
trading signal generation. Concrete strategy implementations (Mean Reversion,
Trend Following, Opening Range Breakout) are added in subsequent tasks.

Requirements: 4.1, 4.5
"""

import uuid
from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from datetime import datetime
from typing import Dict, Optional, Tuple

import pandas as pd

from src.soldier.indicator_engine import IndicatorSet
