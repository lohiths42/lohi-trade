"""Market Profile — Pydantic models for multi-country market configuration.

Defines the complete schema for a market profile including trading hours,
tax rules, available brokers, news sources, and exchange-specific settings.
Each supported country has a pre-built profile that can be customized.

The MarketProfile is the single source of truth for all country-specific
behavior in the trading engine, RMS, and UI.
"""

from __future__ import annotations

from datetime import time
from enum import Enum

from pydantic import BaseModel, Field, field_validator

# ── Enums ───────────────────────────────────────────────────────────────────


class Country(str, Enum):
    """Supported countries for trading."""

    INDIA = "IN"
    UNITED_STATES = "US"
    UNITED_KINGDOM = "UK"
    AUSTRALIA = "AU"
    CANADA = "CA"
    GERMANY = "DE"
    JAPAN = "JP"
    SINGAPORE = "SG"
    HONG_KONG = "HK"
    BRAZIL = "BR"
    FRANCE = "FR"
    SOUTH_KOREA = "KR"
    NETHERLANDS = "NL"
    SWITZERLAND = "CH"
    SWEDEN = "SE"


class Exchange(str, Enum):
    """Supported stock exchanges."""

    # India
    NSE = "NSE"
    BSE = "BSE"
    # United States
    NYSE = "NYSE"
    NASDAQ = "NASDAQ"
    # United Kingdom
    LSE = "LSE"
    # Australia
    ASX = "ASX"
    # Canada
    TSX = "TSX"
    # Germany
    XETRA = "XETRA"
    FRA = "FRA"
    # Japan
    JPX = "JPX"
    TSE_JP = "TSE_JP"
    # Singapore
    SGX = "SGX"
    # Hong Kong
    HKEX = "HKEX"
    # Brazil
    B3 = "B3"
    # France
    EURONEXT_PARIS = "EURONEXT_PARIS"
    # South Korea
    KRX = "KRX"
    # Netherlands
    EURONEXT_AMS = "EURONEXT_AMS"
    # Switzerland
    SIX = "SIX"
